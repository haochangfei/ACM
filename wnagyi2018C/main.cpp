/*
一个由小写字母组成的字符串可以看成一些同一字母的最大碎片组成的。
例如,"aaabbaaac"是由下面碎片组成的:'aaa','bb','c'。
牛牛现在给定一个字符串,请你帮助计算这个字符串的所有碎片的平均长度是多少。
输入包括一个字符串s,字符串s的长度length(1 ≤ length ≤ 50),s只含小写字母('a'-'z')
输出一个整数,表示所有碎片的平均长度,四舍五入保留两位小数。
如样例所示: s = "aaabbaaac"
所有碎片的平均长度 = (3 + 2 + 3 + 1) / 4 = 2.25
aaabbaaac  2.25
*/
#include<stdio.h>
#include<string.h>
#define maxn 55
char ch[maxn];
int main()
{
    int type=1;
    int len=0;
    double n=0;
    gets(ch);
    len=strlen(ch);
    for(int i=1;i<len;i++)
    {
        if(ch[i]!=ch[i-1])
            type++;
    }
    n=(double)len/type;
    printf("%.2lf\n",n);
    return 0;
}
